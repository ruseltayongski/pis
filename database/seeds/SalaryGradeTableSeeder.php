                    $salaryArray = [
                        "salary_tranche" => $tranche[$indexTranche],
                        "salary_grade" => $indexGrade,
                        "salary_step" => $indexStep,
                        "salary_amount" => str_replace(',','',$amount[$indexTranche][$indexGrade][$indexStep]),
                        "status" => 1
                    ];
                    SalaryGrade::create($salaryArray);
                }
            }
        }

